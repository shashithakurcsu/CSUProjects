# This is a Python to find the multiplication and division of two numbers.
# Ask the user to input two numbers (num1 and num2).
num1 = int(input("Enter the first number: "))
num2 = int(input("Enter the second number: "))
#Add them together to find the output. 
num_multiplied = num1 * num2
#Subtract the two numbers to find the output.
num_divided = num1 / num2

# Display the results
print(f"Multiplication result: {num1} * {num2} = {num_multiplied}")
print(f"Division result: {num1} / {num2} = {num_divided}")

# End of program
